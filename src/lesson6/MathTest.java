package lesson6;

public class MathTest {
    public static void main(String[] args) {
        System.out.println(Math.E);
        System.out.println(Math.PI);
        System.out.println(Math.abs(-579.45)); // 絕對值
        System.out.println(Math.max(658.7, 999.8));
        System.out.println(Math.min(658.7, 999.8));
        System.out.println(Math.pow(2, 5)); // 2 的 5 次方
        System.out.println(Math.random()); // 0.0~1.0(不含)
        System.out.println(Math.sqrt(3 * 3)); // 平方根
        System.out.println(Math.cbrt(3 * 3 * 3)); // 立方根
    }
}
